package sec01.exam01;

public class VariableInitializationExample {
	public static void main(String[] args) {
		// declare variable 'value'
		int value;
		
		// read value of variable 'value' and operate arithmetic operation which adds 10
		// assign result value of operation as initial value of variable 'result'
		int result = value + 10;
		
		// read variable 'result' and print to the console
		System.out.println(result);
	}
}